    // carrega participantes por evento ////////////////////////////////////
    private void carregaConvidados(Evento evento) {
        List<Cadastro> cadatros = cadastroDao.retornaPorEventoParticipantes(evento);
        List<Pagamento> pagamentos = pagamentoDao.getPagamentoPorEvento(evento);
        modelo = (DefaultTableModel) grelha.getModel();
        modelo.setNumRows(0);
        double valorArrecadado = 0.0;

        for (Cadastro cadatro : cadatros) {
            modelo.addRow(new Object[]{
                cadatro.getParticipante().getId(),
                cadatro.getParticipante().getNome(),
                cadatro.getParticipante().getDataNascimento(),
                cadatro.getParticipante().getSexo(),
                cadatro.getEvento().getPagamentoList()

            }
            );

        }
        
        
        for (Pagamento pagamento : pagamentos) {
            valorArrecadado = valorArrecadado + pagamento.getValor();
        }

        // verifica quantidade disponivel
        Integer vaga = Integer.parseInt(txtVaga.getText());
        Integer vagasUsadas = cadatros.size();
        Integer vagaDisponivel = vaga - vagasUsadas;

        txtVagasUsadas.setText(vagasUsadas.toString());
        txtVagaDisponiel.setText(vagaDisponivel.toString());
        txtValorPorPessoa.setText(evento.getValorPessoa().toString());
        txtValorArrecadado.setText(valorArrecadado + "");
        
        utilidade.ManipulaMoeda.converteValorDigitadoEmMoeda(txtValorPorPessoa);
        utilidade.ManipulaMoeda.converteValorDigitadoEmMoeda(txtValorArrecadado);

    }

