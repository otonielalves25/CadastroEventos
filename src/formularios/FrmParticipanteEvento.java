    private void carregaEventos() {
        List<Evento> eventos = eventoDao.getListagem();
        cboEvento.removeAllItems();
        cboEvento.addItem("Selecione...");
        for (int i = 0; i < eventos.size(); i++) {
            cboEvento.addItem(eventos.get(i));
        }
    }
