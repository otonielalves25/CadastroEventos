    // CARREGA COMBOBOX DE IGREJAS /////////////////////////////////////////////    
    private void carregaComboboxIgreja() {

        List<Igreja> lista = igrejaDao.getListagem();
        cboIgreja.removeAllItems();
        cboIgreja.addItem("Selecione...");
        for (Igreja igreja : lista) {
            cboIgreja.addItem(igreja);
        }
    }

    //CARREGA GRELHA OTIMIZADA-------------------------------------------------
    public final void carregaGrelha() {

        int contador = 0;
        DefaultTableModel modelo = (DefaultTableModel) grelha.getModel();
        modelo.setNumRows(0);

        List<Celula> lista = celulaDao.getListagem();

        for (Celula celula : lista) {

            modelo.addRow(new Object[]{
                celula.getId(),
                celula.getNomeCelula(),
                celula.getResponsavelCelula(),
                celula.getIgrejaId()});
        }

    }

