    private void carregaTela(){
                try {
            if (grelha.getRowCount() > 0) {
                //
                int codigo = (int) grelha.getValueAt(grelha.getSelectedRow(), 0);

                Celula celula = celulaDao.retornaPorId(codigo);

                txtCodigo.setText(celula.getId() + "");
                txtNome.setText(celula.getNomeCelula());
                txtResponsavel.setText(celula.getResponsavelCelula());
                cboIgreja.setSelectedItem(celula.getIgrejaId());

                btnAlterar.setEnabled(true);
                btnExcluir.setEnabled(true);
                novo = false;
            }

        } catch (Exception e) {
        }
    }
    
