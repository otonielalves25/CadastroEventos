    private void carregaPesquisa() {
        List<Evento> listagem;
        if (radNome.isSelected()) {
            listagem = eventoDao.getListagemLikeNome(txtPesquisa.getText());
        } else if (radLocal.isSelected()) {
            listagem = eventoDao.getListagemLikeLocal(txtPesquisa.getText());
        } else {
            listagem = eventoDao.getListagemLikeData(txtPesquisa.getText());
        }

        modeloTabela.setNumRows(0);
        for (Evento evento2 : listagem) {
            modeloTabela.addRow(new Object[]{
                evento2.getId(),
                evento2.getNome(),
                evento2.getLocalEvento(),
                evento2.getDataEvento()

            });
        }
    }

