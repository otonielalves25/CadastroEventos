    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        // TODO add your handling code here:
        if (ValidarCampos.validarCampo(txtNome, "Nome Igreja")) {
            return;
        }

        if (ValidarCampos.validarCampo(txtCidade, "Cidade")) {
            return;
        }

        if (ValidarCampos.validarCampo(cboEstado, "Estado")) {
            return;
        }

        // CRIA A CLASSE MARCA MODELO //////////////////////////////////////////
        Igreja igreja = new Igreja();
        igreja.setNomeIgreja(txtNome.getText().toUpperCase());
        igreja.setRua(txtRua.getText());
        igreja.setNumero(txtNumero.getText());
        igreja.setCidade(txtCidade.getText());
        igreja.setBairro(txtBairro.getText());
        igreja.setEstado(cboEstado.getSelectedItem().toString());
        igreja.setPastor(txtPastor.getText());

        // CADATRAO NOVO NO BANCO //////////////////////////////////////////////
        if (novo) {
            Igreja igrejaRetornada = igrejaDao.getIgrejaPorNome(txtNome.getText().toUpperCase());
            if (igrejaRetornada != null) {
                JOptionPane.showMessageDialog(this, txtNome.getText() + " já tem cadastro.", null, JOptionPane.ERROR_MESSAGE);
                return;
            }
            igrejaDao.gravar_atualizar(igreja);
            JOptionPane.showMessageDialog(this, "Cadatrado com Sucesso !!!", null, JOptionPane.INFORMATION_MESSAGE);
            // ALTERAR CADASTRO NO BANCO ///////////////////////////////////////
        } else {

            igreja.setId(Integer.parseInt(txtCodigo.getText()));

            igrejaDao.gravar_atualizar(igreja);
            JOptionPane.showMessageDialog(this, "Alterada com Sucesso !!!", null, JOptionPane.INFORMATION_MESSAGE);
            // PEGANDO LOGS DO SISTEMA 

        }

        botaoInicial();
        novo = false;
        habilitado(false);
        limparTudo();
        carregaGrelha();

    }//GEN-LAST:event_btnSalvarActionPerformed

