    public final void carregaGrelha() {

        int contador = 0;
        DefaultTableModel modelo = (DefaultTableModel) grelha.getModel();
        modelo.setNumRows(0);

        List<Igreja> lista = igrejaDao.getListagem();

        for (Igreja igreja : lista) {

            modelo.addRow(new Object[]{
                igreja.getId(),
                igreja.getNomeIgreja(),
                igreja.getCidade(),
                igreja.getEstado(),});
        }

    }

