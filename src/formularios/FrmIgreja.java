                int codigo = (int) grelha.getValueAt(grelha.getSelectedRow(), 0);

                Igreja igreja = igrejaDao.getPorId(codigo);

                txtCodigo.setText(igreja.getId() + "");
                txtNome.setText(igreja.getNomeIgreja());
                txtRua.setText(igreja.getRua());
                txtNumero.setText(igreja.getNumero());
                txtBairro.setText(igreja.getBairro());
                txtCidade.setText(igreja.getCidade());
